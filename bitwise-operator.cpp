#include <iostream>
using namespace std;

int main()
{
    cout << (4 & 8) << endl;   // & - and operator
    cout << (4 | 8) << endl;   // | - or operator
    cout << (4 ^ 8) << endl;   // ^ - xor operator
    cout << (4 << 1) << endl;  // << -  left shift operator
    cout << (10 >> 1) << endl; // << -  left shift operator

    return 0;
}